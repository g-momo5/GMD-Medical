async function renderReportToPath(payload, patient, visitId, reportPath) {
  const template = await readFile(TEMPLATE_PATH);
  const data = buildReportData(payload, patient, visitId);
  const zip = new PizZip(template);
  const doc = new Docxtemplater(zip, {
    paragraphLoop: true,
    linebreaks: true
  });

  doc.render(data);

  const content = doc.getZip().generate({ type: 'uint8array' });
  const finalPath = reportPath.toLowerCase().endsWith('.docx') ? reportPath : `${reportPath}.docx`;
  await writeFile(finalPath, content);
  return finalPath;
}

