function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function buildSingleLinePattern(value) {
  return new RegExp(escapeRegExp(value).replace(/\s+/g, '\\s+'));
}

